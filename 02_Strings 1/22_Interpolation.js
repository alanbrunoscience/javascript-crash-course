/**
 * Interpolation
 * 
 * - Template strings support interpolation! This means you could write a variable in your string, 
 * and get its value. The syntax is straightforward, you wrap your variable name with a dollar sign 
 * and curly braces. Let's take an example where we have a variable language with a value of JavaScript.
 * 
 */

let language = "JavaScript";
console.log(`I am learning ${language}`); // "I am learning JavaScript";

// Remember that string interpolation only works with backticks.