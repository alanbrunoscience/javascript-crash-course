/**
 * Plus Operator
 * 
 * In JavaScript, the plus operator (+) will behave differently based on the types of values you use it with.
 * 
 * You've already seen that 1 + 3 will return the number 4. However, you could also use the + to concatenate 2 strings together, 
 * which means merging them together into 1 string.
 * 
 */

'use strict';

console.log("Hello" + "World"); // HelloWorld

let prefix = "Mrs.";
let name = "Sam";
let string = prefix + " " + name;

console.log(string); // Mrs. Sam