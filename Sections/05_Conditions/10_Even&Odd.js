'use strict';

// even numbers
console.log("*** Even Numbers ***\n");
console.log(`4 % 2 = ${4 % 2}`); // 0
console.log(`6 % 2 = ${6 % 2}`); // 0
console.log(`8 % 2 = ${8 % 2}`); // 0
console.log(`10 % 2 = ${10 % 2}`); // 0

// odd numbers
console.log("\n\n*** Odd Numbers ***\n");
console.log(`3 % 2 = ${3 % 2}`); // 1
console.log(`5 % 2 = ${5 % 2}`); // 1
console.log(`7 % 2 = ${7 % 2}`); // 1
console.log(`9 % 2 = ${9 % 2}`); // 1